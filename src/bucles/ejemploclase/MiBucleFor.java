package bucles.ejemploclase;

public class MiBucleFor {

	public static void main(String[] args) {
		
		//Primero se hace la inicialización sólo una vez
		//Luego se ejecutan las instrucciones,lo que hay dentro de las llaves
		//Luego se hace el incremento
		//Y por último se evalua la condición
		
		for (int i = 1; i <= 10; i++) {
			System.out.println(i);
			
		}
		
		
	}

}
